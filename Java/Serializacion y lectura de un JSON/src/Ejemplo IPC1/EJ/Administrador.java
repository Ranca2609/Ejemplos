    public void TablaCarros(){
        
        int contador = 0;
        try{
            for(Carro carro: Serializacion.carros){
                TCarros.insertRow(contador, carro.getArregloDatos());
            }
        
        }catch(Exception e){
            System.out.println("Ocurrio un error inesperado");
            e.printStackTrace();
        }
    
    }
    
    
    
    public String Abrir(File archivo){
        String contenido= "";
        try{
            entrada = new FileInputStream(archivo);
            int ads;
            while((ads = entrada.read())!= -1){
                char caracteres = (char)ads;
                contenido+= caracteres;       
            }
        }catch(Exception e){
        }
        return contenido;
    }
    
   
  
